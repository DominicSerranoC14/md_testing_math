//operator functions
var add = function( one, two ) {
  return one + two;
}

var subtract = function( one, two ) {
  return one - two;
}

var multiply= function( one, two ) {
  return one * two;
}

var divide = function( one, two ) {
  return one / two;
}

var square = function( one ) {
  return one * one;
}

var squareRoot = function( one ) {
  return Math.sqrt(one);
}
